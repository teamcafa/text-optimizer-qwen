export function buildPrompt(inputText: string, values: Record<string, number>) {
  const dims = Object.entries(values || {})
    .filter(([, v]) => Number(v) > 0)
    .sort((a, b) => Number(b[1]) - Number(a[1]))
    .map(([k, v]) => `${k}:${v}`)
    .join(", ");

  return `你是一个中文表达风格引擎。根据给定维度改写文本，只输出最终结果，不要解释。

维度参数（0-100，越高越突出）:
${dims || "无"}

改写要求：
1. 保留原意，但显著增强表达冲击力与可读性
2. 允许适度夸张、反讽、对比、隐喻，增强戏剧性
3. 语言尽量有记忆点，有“可截图感”
4. 不要输出分析、标题、引号或说明

【危险模式（默认开启）】：
- 在不违反基本规范的前提下，放大语气与态度
- 优先生成更锋利、更有梗、更带情绪的表达
- 允许“差一点点越界”的边界感（但避免直接攻击个人或群体）
- 当有多个表达路径时，选择更大胆、更有传播性的那一个

额外约束：
- 哲学风格使用现代中文表达，不要转为佛经、玄学或文言文
- 除非佛学指数很高，否则避免空性、缘起等宗教口吻

原文：
${inputText}`;
}